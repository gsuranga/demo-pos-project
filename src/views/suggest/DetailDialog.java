    public void counting_method() {
        int rowCount = dtm.getRowCount();
        double total = 0;
        for (int i = 0; i < rowCount; i++) {
            double amount = Double.parseDouble(String.valueOf(dtm.getValueAt(i, 5)));
            total = total + amount;
            System.out.println(amount);
        }
        double discount = Double.parseDouble(discount_field.getText());
        double vat = Double.parseDouble(vat_field.getText());
        double discount_amount = total * discount / 100;
        double vat_amount = total * vat / 100;
        tawd.setText(String.format("%.2f", (total) - (discount_amount))); 
        tawv.setText(String.format("%.2f", (total) - (discount_amount) + vat_amount));
    }
