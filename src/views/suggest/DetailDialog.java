    private void acceptorderbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptorderbuttonActionPerformed

        int showConfirmDialog = JOptionPane.showConfirmDialog(this, "Are you sure you want Accept this order ?");
        if (showConfirmDialog == 0) {
            try {
                int row_count = dtm.getRowCount();
                for (int i = 0; i < row_count; i++) {
                    String part_no = String.valueOf(dtm.getValueAt(i, 1));
                    String qty = String.valueOf(dtm.getValueAt(i, 3));
                    String unit_price = String.valueOf(dtm.getValueAt(i, 4));

                    JSONObject a = new JSONObject();
                    a.put("part_no", part_no);
                    a.put("qty", qty);
                    a.put("unit_price", unit_price);
                    obj.put(a);

                }
                String json = obj.toString();

                try {
                    double tawd_double = Double.parseDouble(tawd.getText());
                    double tawv_double = Double.parseDouble(tawv.getText());
                    String result = SuggestController.accept_purchase_order(purchase_order_id, json, tawd_double, tawv_double);
                    System.out.println("result:"+result);
                    int res = Integer.parseInt(result);
                    if (res > 0) {
                        JOptionPane.showMessageDialog(this, "Successfully Sent");
                        this.dispose();
                        suggestPanel.send();
                    } else {
                        JOptionPane.showMessageDialog(this, "Not Sent Try Again..");
                    }
                } catch (IOException ex) {
                    Logger.getLogger(DetailDialog.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(DetailDialog.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(DetailDialog.class.getName()).log(Level.SEVERE, null, ex);
                }

            } catch (JSONException ex) {
                Logger.getLogger(DetailDialog.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            System.out.println("No");
        }


    }//GEN-LAST:event_acceptorderbuttonActionPerformed

