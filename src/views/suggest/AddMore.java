    private void ok_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ok_buttonActionPerformed
        int select_row = part_table.getSelectedRow();
        String part_no = String.valueOf(dtm.getValueAt(select_row, 0));
        String description = String.valueOf(dtm.getValueAt(select_row, 1));
        double buying_price = Double.parseDouble(String.valueOf(dtm.getValueAt(select_row, 2)));
        int qty=Integer.parseInt(qty_text_field.getText());
        Object ro[]={0,part_no,description,qty,buying_price,qty*buying_price};
        dtm0.addRow(ro);
        detailDialog.counting_method();
        this.dispose();
        
    }//GEN-LAST:event_ok_buttonActionPerformed

