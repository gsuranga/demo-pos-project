    private void btnUpdateProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateProfileActionPerformed
        DealerProfile dealerProfile = new DealerProfile();

        dealerProfile.setDealerID(dealerID);
        dealerProfile.setDealerName(txtUpdateDealerName.getText());
        dealerProfile.setDealerMobileNo(txtUpdateMobile.getText());
        dealerProfile.setEmailAddress(txtUpdateEMail.getText());
        dealerProfile.setAddress(txtUpdateAddress.getText());
        try {
            String dealerName = txtUpdateDealerName.getText();
            if (dealerName.equals("") || dealerName.equals(null) || dealerName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Dealer name cannot be empty.", "Empty input", JOptionPane.WARNING_MESSAGE);

            } else {
                int updateProfile = ProfileController.updateProfile(dealerProfile);
                if (updateProfile > 0) {
                    JOptionPane.showMessageDialog(this, "Profile successfully updated.", "Success", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                }
            }

        } catch (ClassNotFoundException ex) {
        } catch (SQLException ex) {
        }

    }//GEN-LAST:event_btnUpdateProfileActionPerformed

