    void setUpdateVehicle(Vehicle vehicle) {
        vehicleID = vehicle.getVehicleID();
        cmbCustomer.setSelectedItem(vehicle.getCustName().trim());
        cmbVModel.setSelectedItem(vehicle.getModelName().trim());
        String fullRegNo = vehicle.getVehicleRegNo().trim();
        String regSplits[] = fullRegNo.split("-");
        txtReg1.setText(regSplits[0]);
        txtReg2.setText(regSplits[1]);
        txtReg3.setText(regSplits[2]);

    }
