    public void fillPartTypes() throws ClassNotFoundException, SQLException {
        ResultSet rstItemTypes = VehicleModelController.getModelDetails("status", (1 + ""));
        cmbVModel.removeAllItems();
        while (rstItemTypes.next()) {
            VehicleModel vm = new VehicleModel();
            vm.setModelName(rstItemTypes.getString(2));
            vm.setModelID(rstItemTypes.getInt(1));
            cmbVModel.addItem(vm);
        }
    }

    public void updateVehicle() throws ClassNotFoundException, SQLException {
        String selectedItem = cmbCustomer.getSelectedItem().toString();
        ResultSet customerDetails = CustomerController.getCustomerDetails("customer_name", selectedItem);
        customerDetails.next();
        int custID = customerDetails.getInt(1);

        String regNo = txtReg1.getText() + "-" + txtReg2.getText() + "-" + txtReg3.getText();
        String vModel = cmbVModel.getSelectedItem().toString();
        int modelID = ((VehicleModel) cmbVModel.getSelectedItem()).getModelID();

        if (regNo == null || regNo == "" || regNo == " " || regNo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Field is empty", "Error", JOptionPane.ERROR_MESSAGE);
        }
        Vehicle vehicle = new Vehicle();
        vehicle.setVehicleID(vehicleID);
        vehicle.setCustomerID(custID);
        vehicle.setVehicleModelID(modelID);
        vehicle.setVehicleRegNo(regNo);
        int addNewVehicle = VehicleController.updateVehicle(vehicle);
        if (addNewVehicle > 0) {
            JOptionPane.showMessageDialog(this, "Vehicle updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }
    }

