    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if (userNameField.getText().isEmpty()|passwordField.getPassword().toString().isEmpty()|userName2.getText().isEmpty()|name2.getText().isEmpty()|password2.getPassword().toString().isEmpty()) {
            JOptionPane.showMessageDialog( null, "Please Complete Fields", "Error", JOptionPane.ERROR_MESSAGE);
        } else {

            String userName = userNameField.getText();
            char[] ch = passwordField.getPassword();
            String password = new String(ch);

            User user = new User();
            user.setUserName(userName);
            user.setPasword(password);

            try {
                User searchUser = UserController.searchUser(user);

                if (searchUser.getId() > 0) {
                    User user2 = new User();
                    user2.setUserName(userName2.getText());
                    user2.setName(name2.getText());
                    char[] ch2 = password2.getPassword();
                    String password3 = new String(ch2);
                    user2.setPasword(password3);
                    int updateUser = UserController.updateUser(user2, userName);
                    if (updateUser > 0) {
                        JOptionPane.showMessageDialog(null, "Updated User");
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog( null, "Not Updated", "Failure", JOptionPane.ERROR_MESSAGE);
                    }


                } else {
                    JOptionPane.showMessageDialog(null, "Invalied User Name / Pasdsword", "Error", JOptionPane.ERROR_MESSAGE);
                    
                }

            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "Class Error");

            } catch (SQLException ex) {
                ex.printStackTrace();

            }
        }

    }//GEN-LAST:event_updateButtonActionPerformed

