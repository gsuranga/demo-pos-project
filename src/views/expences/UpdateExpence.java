    public void updateExpence() {
        try {
            String description = txtUDescription.getText();
            double amount = Double.parseDouble(txtUAmount.getText());
            Date d1 = new Date();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String addedDate = sdf.format(d1);
            String expenceDate = sdf.format(updateExpenceDateCh.getDate());
            sdf.applyPattern("HH:mm:ss");
            String addedTime = sdf.format(d1);

            if ("".equals(description) || description == null || description.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter description.", "No Description", JOptionPane.ERROR_MESSAGE);
            } else if (amount == 0) {
                JOptionPane.showMessageDialog(this, "Amount cannot be 0 or minus", "Wrong Amount", JOptionPane.ERROR_MESSAGE);
            } else if (expenceDate == "" || expenceDate == null || expenceDate.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please select a date", "Empty date", JOptionPane.ERROR_MESSAGE);
            } else {
                Expence expence2 = new Expence();
                expence2.setExpenceID(expence.getExpenceID());
                expence2.setDescription(description);
                expence2.setAmount(amount);
                expence2.setExpenceDate(expenceDate);

                int value = ExpenceController.updateExpence(expence2);
                if (value > 0) {
                    JOptionPane.showMessageDialog(this, "Expence updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Please check your values again.", "Error in values", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

    public void setValues(Expence ex) throws ParseException {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        updateExpenceDateCh.setDate(sdf.parse(ex.getExpenceDate()));
        txtUAmount.setText(ex.getAmount() + "");
        txtUDescription.setText(ex.getDescription());
    }

