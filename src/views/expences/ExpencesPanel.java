    public void deleteExpence() {

        int[] selectedRows = tblAllExpences.getSelectedRows();

        try {
            while (selectedRows.length > 0) {
                int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this expence...?", "Delete", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                int value = 0;
                if (option == 0) {
                    Expence dExpence = new Expence();
                    dExpence.setExpenceID(Integer.parseInt(tblAllExpences.getValueAt(selectedRows[0], 0).toString()));
                    value = ExpenceController.deleteExpence(dExpence);
                    expenceModel.removeRow(selectedRows[0]);
                    selectedRows = tblAllExpences.getSelectedRows();

                    if (value > 0) {
                        JOptionPane.showMessageDialog(this, "Expence successfully deleted", "Succeed", JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {
                    tblAllExpences.clearSelection();
                    //      checkCustomerButtons();
                    break;

                }

            }
        } catch (ClassNotFoundException classNotFoundException) {
        } catch (SQLException sQLException) {
        }
    }

    public void checkButtons() {
        int rowCount = tblAllExpences.getSelectedRowCount();
        if (rowCount > 0) {
            btnDelete.setEnabled(true);
            btnUpdate.setEnabled(true);
        } else {
            btnDelete.setEnabled(false);
            btnUpdate.setEnabled(false);
        }
    }

    public double calculateTotalExpences() {
        double expences = 0;
        int rows = tblAllExpences.getRowCount();
        for (int i = 0; i < rows; i++) {
            expences += Double.parseDouble(tblAllExpences.getValueAt(i, 2).toString());

        }
        return expences;
    }

