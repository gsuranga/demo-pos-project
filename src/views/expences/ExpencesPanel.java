    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            int selectedRows = tblAllExpences.getSelectedRowCount();
            int selected = tblAllExpences.getSelectedRow();
            if (selectedRows > 0) {
                int expenceID = Integer.parseInt(tblAllExpences.getValueAt(selected, 0).toString());
                String date = tblAllExpences.getValueAt(selected, 3).toString().trim();
                double amount = Double.parseDouble(tblAllExpences.getValueAt(selected, 2).toString());
                String desc = tblAllExpences.getValueAt(selected, 1).toString();
                Expence expence = new Expence();
                expence.setAmount(amount);
                expence.setDescription(desc);
                expence.setExpenceDate(date);
                expence.setExpenceID(expenceID);
                UpdateExpence uExpence = new UpdateExpence(new JFrame(), true, expence);
                uExpence.setVisible(true);
                ExpenceController.loadAllExpences(expenceModel);
                checkButtons();

            }
        } catch (NumberFormatException numberFormatException) {
        } catch (HeadlessException headlessException) {
        } catch (ClassNotFoundException classNotFoundException) {
        } catch (SQLException sQLException) {
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

