    public int addNewCustomer() {
        int value = 0;
        try {

            String customerName = txtCustName.getText();
            String contactNo = txtContactNo.getText();
            String address = txtCustAddress.getText();
            int custType = CustomerController.getCustomertypeForName(cmbCustType.getSelectedItem().toString());
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String addedDate = sdf.format(new Date());
            sdf.applyPattern("HH:mm:ss");
            String addedTime = sdf.format(new Date());
            int status = 1;
            String accountNo = txtAccountNo.getText();

            if (customerName == "" || customerName == null || customerName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter customer name", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                Customer customer = new Customer(customerName, contactNo, address, custType, accountNo, addedDate, addedTime, status);
                value = CustomerController.addNewCustomer(customer);
            }

            if (value > 0) {
                JOptionPane.showMessageDialog(this, "New customer successfully added", "Successful", JOptionPane.INFORMATION_MESSAGE);
                refreshFields();
                //CustomerController.fillCustomerNames(customerCombo);
            }

        } catch (NumberFormatException numberFormatException) {
            numberFormatException.printStackTrace();
            JOptionPane.showMessageDialog(this, "Check your entered values again..", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException classNotFoundException) {
        } catch (SQLException sQLException) {
            sQLException.printStackTrace();
            JOptionPane.showMessageDialog(this, "Check your entered values again..", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return value;
    }

    public void loadAllCustomerTypes() throws ClassNotFoundException, SQLException {
        ResultSet customertypes = CustomerController.getCustomertypes();
        cmbCustType.removeAllItems();
        while (customertypes.next()) {
            cmbCustType.addItem(customertypes.getString(1));
        }
    }
