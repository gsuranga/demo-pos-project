    private void cmbCustTypePopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_cmbCustTypePopupMenuWillBecomeInvisible
        String typeCode = "";
        try {
            String selected = cmbCustType.getSelectedItem().toString();
            ResultSet customerTypeData = CustomerTypeController.getCustomerTypeData("customer_type_name", selected);
            customerTypeData.next();
            typeCode = customerTypeData.getString(3);
            txtAccountNo.setText(AutoGenerateID.generateNextID(typeCode, "customer"));
            txtCustAddress.requestFocus();
        } catch (ClassNotFoundException ex) {

        } catch (SQLException ex) {
            txtAccountNo.setText(AutoGenerateID.generateNextDBIDOnEmptyResultSet(typeCode));
        }
    }//GEN-LAST:event_cmbCustTypePopupMenuWillBecomeInvisible

