    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String regNo = txtReg1.getText() + "-" + txtReg2.getText() + "-" + txtReg3.getText();
        String vModel = cmbVehicleModel.getSelectedItem().toString();
        int modelID = ((VehicleModel) cmbVehicleModel.getSelectedItem()).getModelID();
        if (regNo == null || regNo == "" || regNo == " " || regNo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Field is empty", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String vehicleRegNo[] = {regNo, vModel, (modelID + "")};
            vehicleDtm.addRow(vehicleRegNo);
            txtReg1.setText("");
            txtReg2.setText("");
            txtReg3.setText("");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int val = addNewCustomer();
            int rowCount = tblVehicleRegNo.getRowCount();
            ArrayList vehicleCount = new ArrayList();

            if (val > 0 && rowCount > 0) {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String addedDate = sdf.format(new Date());
                sdf.applyPattern("HH:mm:ss");
                String addedTime = sdf.format(new Date());

                int lastCustomer = CustomerController.getLastInsertedCustomerID();
                for (int i = 0; i < rowCount; i++) {
                    String vehicleRegNo = tblVehicleRegNo.getValueAt(i, 0).toString();
                    int modelNo = Integer.parseInt(tblVehicleRegNo.getValueAt(i, 2).toString());
                    Vehicle vehicle = new Vehicle(lastCustomer, vehicleRegNo, modelNo, addedDate, addedTime, 1);
                    vehicleCount.add(VehicleController.addNewVehicle(vehicle));
                }
                if (rowCount == vehicleCount.size()) {
                    JOptionPane.showMessageDialog(this, "Vehicles successfully added", "Successful", JOptionPane.INFORMATION_MESSAGE);
                    vehicleDtm.setRowCount(0);

                }

            }
        } catch (ClassNotFoundException ex) {
        } catch (SQLException ex) {
        } finally {
            dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

