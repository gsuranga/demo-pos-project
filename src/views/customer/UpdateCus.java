    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Customer customer = new Customer();
            customer.setCustomerID(customerid);
            customer.setCustomerName(txt_cust_name.getText());
            customer.setAccountNo(txt_acount_no.getText());
            customer.setContactNo(txt_contact_no.getText());
            customer.setAddress(txt_address.getText());
            String type = comboCustomer.getSelectedItem().toString();
            ResultSet rst = CustomerTypeController.getCustomerTypeData("customer_type_name", type);
            rst.next();
            int typeID = rst.getInt(1);
            customer.setCustomerType(typeID);
            int updateCustomer = CustomerController.updateCustomer(customer);
            if (updateCustomer > 0) {
                JOptionPane.showMessageDialog(this, "Customer successfully updated", "Successful", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
        } catch (ClassNotFoundException ex) {
        } catch (SQLException ex) {
            ex.printStackTrace();
        }


    }//GEN-LAST:event_jButton1ActionPerformed

