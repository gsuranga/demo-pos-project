    private void btnSetDateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetDateActionPerformed
        Date date = endDateChooser.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String endDate = sdf.format(date);
        Debt debt = new Debt();
        debt.setEndDate(endDate);
        debt.setDebtID(debtID);
        try {
            int updateDebtEndDate = DebtController.updateDebtEndDate(debt);
            if (updateDebtEndDate > 0) {
                dispose();
            }
        } catch (ClassNotFoundException ex) {
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSetDateActionPerformed

