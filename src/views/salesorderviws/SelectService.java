    private void txtServiceSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtServiceSearchKeyReleased
        try {
            final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(serviceDtm);
            tblSelectService.setRowSorter(sorter);
            sorter.setRowFilter(RowFilter.regexFilter(("(?i)" + txtServiceSearch.getText())));
            int keyCode = evt.getKeyCode();

            if (keyCode == KeyEvent.VK_DOWN) {
                tblSelectService.requestFocus();
                tblSelectService.changeSelection(0, 0, false, false);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_txtServiceSearchKeyReleased

