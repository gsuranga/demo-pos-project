    public void addNewServicesToTable() throws ClassNotFoundException, SQLException {
        //String custName = ((String) cmbSelectCustomer.getSelectedItem());
        int selectedRow = tblSelectService.getSelectedRow();
        String serviceNo = tblSelectService.getValueAt(selectedRow, 1).toString();
        String description = tblSelectService.getValueAt(selectedRow, 2).toString();
        int serviceID = Integer.parseInt(tblSelectService.getValueAt(selectedRow, 0).toString());
        double cost = Double.parseDouble(tblSelectService.getValueAt(selectedRow, 4).toString());
        double qty = Double.parseDouble(txtServiceQty.getText());
        
        
        double discount = 0.00;
        double discountAmount = 0.00;
        double amount = 0.00;
        int categoryID = 0;
        int mnemonic = 0;
        int discountType = 0;
        mnemonic = buttonGroup1.getSelection().getMnemonic();
        System.out.println(mnemonic);
        String doneBy = txtDoneBy.getText();
        if (mnemonic == 49) {
            discountType = 1;
            discount = Double.parseDouble(txtDiscount.getText());
            discountAmount = ((cost * qty) * discount / 100);
            amount = (cost * qty) - discountAmount;
        } else if (mnemonic == 50) {
            discount = Double.parseDouble(txtDiscountAmount.getText());
            discountAmount = discount;
            amount = (cost * qty) - discountAmount;
        } else {
            discount = 0.00;
            discountAmount = 0.00;
            amount = cost * qty;

        }
        if (serviceNo == null || serviceNo == "") {
            JOptionPane.showMessageDialog(this, "Please select a service to add", "Error in selection", JOptionPane.WARNING_MESSAGE);

        } else if (qty <= 0) {
            JOptionPane.showMessageDialog(this, "Quantity cannot be 0 or less.", "Invalid quantity", JOptionPane.WARNING_MESSAGE);
        } else {
            Object obj[] = {serviceID, serviceNo, description, qty, String.format("%.2f", discountAmount), String.format("%.2f", cost), String.format("%.2f", amount), discountType, doneBy, String.format("%.2f", discount)};
            orderServicesDtm.addRow(obj);
            clearFields();
            dispose();
        }
    }

