    public void addNewItemsToTable() throws ClassNotFoundException, SQLException {
        DecimalFormat df = new DecimalFormat("#.00");
        df.setMaximumFractionDigits(2);
        int selectedRow = tblSelectItem.getSelectedRow();
        String partNo = tblSelectItem.getValueAt(selectedRow, 1).toString();
        String description = tblSelectItem.getValueAt(selectedRow, 2).toString();
        int itemCode = Integer.parseInt(tblSelectItem.getValueAt(selectedRow, 0).toString());
        double buyingPrice = Double.parseDouble(tblSelectItem.getValueAt(selectedRow, 3).toString());
        double formattedBuyingPrice = Double.parseDouble(df.format(buyingPrice));
        int quantity = Integer.parseInt(txtQty.getText().trim());

        double amount = Double.parseDouble(df.format(buyingPrice * quantity));

        if (partNo == null || partNo == "") {
            JOptionPane.showMessageDialog(this, "Please select an item to add", "Error in items", JOptionPane.WARNING_MESSAGE);
        } else if (quantity == 0 || txtQty.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Quantity cannot be empty or 0", "Error in quantity", JOptionPane.WARNING_MESSAGE);
        } else {
            Object obj[] = {itemCode, partNo, description, formattedBuyingPrice, quantity, amount};
            tblSelectedDtm.addRow(obj);
            //clearFields();
            //mainPanel.checkButtons();
            //mainPanel.checkBillButton();
            // mainPanel.calculate();
            dispose();
        }

    }

