    public void loadAllTataPartsToSelect(DefaultTableModel allPartsDtm) throws ClassNotFoundException, SQLException {
        ResultSet data = ItemController.getAllTataPartsForPurchase();
        String tableRow[] = new String[10];
        int rowCount = allPartsDtm.getRowCount();
        if (rowCount >= 0) {
            allPartsDtm.setRowCount(0);
        }

        while (data.next()) {
            for (int i = 0; i < 10; i++) {
                if (i == 9) {
                    tableRow[i] = "All";
                } else {
                    tableRow[i] = data.getString(i + 1);
                }

            }
            allPartsDtm.addRow(tableRow);
        }
    }

